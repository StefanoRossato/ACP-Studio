local monitor

----------------------------------------------------------------------
-- Private Methods
----------------------------------------------------------------------

local function ClearLog()

    reaper.ClearConsole()

end

local function Log(message)

    reaper.ShowConsoleMsg(message .. "\n")

end

----------------------------------------------------------------------
-- Test Cases
----------------------------------------------------------------------

local function TestCase()

    Log("Creating RuntimeMonitor...")

    monitor = RuntimeMonitor.New()

    assert(monitor ~= nil)

    Log("PASS - RuntimeMonitor created")

    Log("Calling Initialize()...")

    monitor:Initialize()

    Log("PASS - Initialize() completed")

end

----------------------------------------------------------------------
-- Public Interface
----------------------------------------------------------------------

local function Run()

    ClearLog()

    Log("")
    Log("========================================")
    Log("RuntimeMonitor Test")
    Log("========================================")

    TestCase()

    Log("========================================")
    Log("RuntimeMonitor Test PASSED")
    Log("========================================")

end

----------------------------------------------------------------------
-- Module Export
----------------------------------------------------------------------

Run()