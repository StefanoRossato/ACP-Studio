--------------------------------------------------------------------------------
-- Entry Point
--------------------------------------------------------------------------------

local scriptPath =
    debug.getinfo(1, "S").source

local scriptDirectory =
    scriptPath:gsub("^@", ""):match("(.*/)")

local EntryPoint =
    dofile(scriptDirectory .. "../../../Core/Bootstrap/EntryPoint.lua")

assert(
    EntryPoint.Initialize(scriptPath),
    "Failed to initialize execution environment."
)

--------------------------------------------------------------------------------
-- Bootstrap
--------------------------------------------------------------------------------

local Bootstrap =
    require("Core.Bootstrap")

--------------------------------------------------------------------------------
-- Console Helpers
--------------------------------------------------------------------------------

local function Log(message)

    reaper.ShowConsoleMsg(message .. "\n")

end

--------------------------------------------------------------------------------
-- Test
--------------------------------------------------------------------------------

reaper.ClearConsole()

Log("============================================================")
Log("ACP Studio")
Log("Bootstrap Infrastructure Test")
Log("============================================================")
Log("")

--------------------------------------------------------------------------------
-- Initialize
--------------------------------------------------------------------------------

assert(
    Bootstrap.Initialize(),
    "Bootstrap initialization failed."
)

Log("[PASS] Bootstrap initialized.")

--------------------------------------------------------------------------------
-- Shutdown
--------------------------------------------------------------------------------

assert(
    Bootstrap.Shutdown(),
    "Bootstrap shutdown failed."
)

Log("[PASS] Bootstrap shutdown.")

--------------------------------------------------------------------------------
-- Completed
--------------------------------------------------------------------------------

Log("")
Log("Bootstrap Infrastructure Test PASSED")
Log("============================================================")
Log("")