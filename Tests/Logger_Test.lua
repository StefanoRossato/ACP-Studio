--------------------------------------------------------------------------------
-- LOGGER-201
-- Foundation Logger Test
--------------------------------------------------------------------------------

--------------------------------------------------------------------------------
-- Initialize Test Environment
--------------------------------------------------------------------------------

require("Tests.TestBootstrap").Initialize()

--------------------------------------------------------------------------------
-- Dependencies
--------------------------------------------------------------------------------

local Logger =
    require("Core.Foundation.Logger")

--------------------------------------------------------------------------------
-- Execute
--------------------------------------------------------------------------------

assert(
    Logger ~= nil,
    "Logger module not loaded.")

------------------------------------------------------------------------------
-- Lifecycle
------------------------------------------------------------------------------

assert(
    Logger.Initialize(),
    "Logger initialization failed.")

assert(
    not Logger.Initialize(),
    "Logger accepted duplicate initialization.")

------------------------------------------------------------------------------
-- Public API
------------------------------------------------------------------------------

Logger.Log(
    "Generic log message")

Logger.Info(
    "Information message")

Logger.Warning(
    "Warning message")

Logger.Error(
    "Error message")

------------------------------------------------------------------------------
-- Shutdown
------------------------------------------------------------------------------

assert(
    Logger.Shutdown(),
    "Logger shutdown failed.")

assert(
    not Logger.Shutdown(),
    "Logger accepted duplicate shutdown.")

--------------------------------------------------------------------------------
-- Result
--------------------------------------------------------------------------------

reaper.ShowConsoleMsg(
    "LOGGER-201 PASSED\n")