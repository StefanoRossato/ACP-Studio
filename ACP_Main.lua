-- Application Entry Point
----------------------------------------------------------------------

----------------------------------------------------------------------
-- Project Root
----------------------------------------------------------------------

local scriptPath = debug.getinfo(1, "S").source:match("@?(.*[/\\])")

local projectRoot = scriptPath:gsub("Core[/\\]$", "")

----------------------------------------------------------------------
-- Configure Lua Search Path
----------------------------------------------------------------------

package.path = package.path
    .. ";" .. projectRoot .. "?.lua"
    .. ";" .. projectRoot .. "?/init.lua"

----------------------------------------------------------------------
-- Start Application
----------------------------------------------------------------------

local Session = require("Core.Analysis.Session")

Session.Run()