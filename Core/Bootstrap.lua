local Bootstrap = {}

--------------------------------------------------------------------------------
-- Dependencies
--------------------------------------------------------------------------------

local BootstrapCoordinator =
    require("Core.Bootstrap.BootstrapCoordinator")
    
--------------------------------------------------------------------------------
-- Public API
--------------------------------------------------------------------------------

function Bootstrap.Initialize()

    return BootstrapCoordinator.Initialize()

end

--------------------------------------------------------------------------------

function Bootstrap.Shutdown()

    return BootstrapCoordinator.Shutdown()

end

--------------------------------------------------------------------------------
-- Module Return
--------------------------------------------------------------------------------

return Bootstrap