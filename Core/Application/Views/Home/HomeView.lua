local BaseView =
    require("Core.GUI.BaseView")


local HomeView = {}


--------------------------------------------------------------------------------
-- Constructor
--------------------------------------------------------------------------------

function HomeView.New()

    local View =
        BaseView.New(
            "Home",
            "Home"
        )


    setmetatable(
        View,
        {
            __index = HomeView
        }
    )


    return View

end


--------------------------------------------------------------------------------
-- Lifecycle
--------------------------------------------------------------------------------

function HomeView:OnEnter()

    ------------------------------------------------------------------------
    -- View activation
    ------------------------------------------------------------------------

end


function HomeView:OnExit()

    ------------------------------------------------------------------------
    -- View deactivation
    ------------------------------------------------------------------------

end


--------------------------------------------------------------------------------
-- Rendering
--------------------------------------------------------------------------------

function HomeView:Render(ctx)

    ------------------------------------------------------------------------
    -- Home Dashboard Foundation
    ------------------------------------------------------------------------

    reaper.ImGui_Text(
        ctx,
        "ACP Studio"
    )


    reaper.ImGui_Separator(
        ctx
    )


    reaper.ImGui_Text(
        ctx,
        "Home Dashboard"
    )

end

--------------------------------------------------------------------------------
-- End of Module
--------------------------------------------------------------------------------

return HomeView