function ObservationSnapshot.Create(data)

    assert(
        type(data) == "table",
        "ObservationSnapshot data must be a table.")

    local snapshot =
    {
        Runtime =
            data.Runtime or {},

        Observation =
            data.Observation or {},

        Analysis =
            data.Analysis or {},

        SharedMemory =
            data.SharedMemory or {}
    }

    return snapshot

end

