------------------------------------------------------------------------------
-- ACP Studio
-- Toolbar
------------------------------------------------------------------------------

local Toolbar = {}

------------------------------------------------------------------------------
-- Render
------------------------------------------------------------------------------

function Toolbar.Render(context)

    assert(
        context,
        "Toolbar.Render(): context is nil.")

    reaper.ImGui_Text(
        context,
        "Toolbar")

    reaper.ImGui_Separator(context)

end

------------------------------------------------------------------------------
-- Module
------------------------------------------------------------------------------

return Toolbar