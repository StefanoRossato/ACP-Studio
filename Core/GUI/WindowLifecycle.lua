----------------------------------------------------------------------
-- ACP Studio
-- WindowLifecycle
----------------------------------------------------------------------

local WindowLifecycle = {}

----------------------------------------------------------------------
-- Private State
----------------------------------------------------------------------

local isOpen = false

----------------------------------------------------------------------
-- Open
----------------------------------------------------------------------

function WindowLifecycle.Open()
    isOpen = true
end

----------------------------------------------------------------------
-- Close
----------------------------------------------------------------------

function WindowLifecycle.Close()
    isOpen = false
end

----------------------------------------------------------------------
-- Is Open
----------------------------------------------------------------------

function WindowLifecycle.IsOpen()
    return isOpen
end

----------------------------------------------------------------------
-- Module
----------------------------------------------------------------------

return WindowLifecycle